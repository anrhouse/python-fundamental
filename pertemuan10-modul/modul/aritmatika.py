def penjumlahan(a, b):
    c = a+b
    return c

def pengurangan(a, b):
    c = a-b
    return c

def perkalihan(a, b):
    c = a * b
    return c

def pembagian(a, b):
    c = a / b
    return c

def pangkat(a, b):
    c = a ** b
    return c

def pembagian_bulat(a, b):
    c = a // b
    return c

def sisa_bagi(a, b):
    c = a % b
    return c