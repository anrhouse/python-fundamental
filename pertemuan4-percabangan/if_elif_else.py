bilangan = 0

if bilangan > 0:
    print("Bilangan ",bilangan," adalah bilangan positif")
elif bilangan < 0:
    print("Bilangan ",bilangan," adalah bilangan negatif")
else:
    print("Bilangan ",bilangan," adalah bilangan netral/ nol")